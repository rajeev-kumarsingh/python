"""
By leaving out the end index, the range will go to the end
"""
s = "Get the characters from position 2, and all the way to end"
print("Given string is: ", s)
print("Length of the given string is: ", len(s))
print("Characters from 2 to end: ",s[2:])
print("Characters from 10 to end: ", s[10:])
print("Characters from first to end: ", s[0:])
