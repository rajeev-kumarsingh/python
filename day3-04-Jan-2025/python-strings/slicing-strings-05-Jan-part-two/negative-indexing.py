"""
Negative Indexing: Use negative indexing to start the slice from the end of the string.

"""
s = "My name is Rajeev Kumar Singh"
print("Given string is: ", s)
print("length of given string is: ", len(s))
print(s[-5:-2])
print(s[-5:-1])
print(s[-5:0])
print(s[-19:-1])
print(s[-29:-1])
print(s[-29:])

