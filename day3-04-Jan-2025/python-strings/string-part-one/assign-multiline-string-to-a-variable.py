"""You can assign a multiline string to a variable by using three double quotes or three single quotes :
Note: in the result, the line breaks are inserted at the same position as in the code.

"""
x = """Hi, Good morning 
        My name is Rajeev Kumar Singh
         I am working as a DevOps Engineer
          Learning Python to automate my task in DevOps.! """
print(x)

y = '''Hi, This is multiline string 
        assignment to a variable using
        single quotes 
        My name is Rajeev Kumar Singh
         I am working as a DevOps Engineer
          Learning Python to automate my task in DevOps.! '''
print(y)