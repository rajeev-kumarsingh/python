"""
Python has set of built in methods that you can use on strings

The upper() method returns the string in upper case
"""

s = "now we are learning how to modify the strings using python built in method"
s1 = "Python has a lot of built in methods which helps us to modify the string"
print("Given string is: ", s)
print("To change the string in upper case use upper() method ", s.upper())
print("Given second string is: ", s1)
print("Modify the second string in upper case: use upper() ", s1.upper())
