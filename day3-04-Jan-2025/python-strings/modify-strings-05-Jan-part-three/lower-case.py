"""
The lower() method returns the string in lower case
"""
s = "The lower() method returns the string in lower case"
s1 = "THE lower() METHOD RETURNS THE STRING IN LOWER CASE"

print("use lower() method to change the string in lower case")
print("\n")
print("Firt string is: ", s)
print("Second string is: ", s1)
print("\n")
print("First string in lower case, use s.lower() ", s.lower())
print("\n")
print("Second string in lower case, use s1.lower() ", s1.lower())
      