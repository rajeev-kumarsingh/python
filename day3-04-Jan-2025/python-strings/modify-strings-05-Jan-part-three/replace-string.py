# The replace() method replaces a string with another string:
s = 'Rajeev Kumar singh'
print("String: ",s)
print("To replace string use replace() method")
# Replace string
#print(s.replace("Rajeev Kumar singh", "RajeevKumarSingh"))
r = s.replace("Rajeev Kumar singh", "RajeevKumarSingh")
print("Replaced String is: ", r)
# Replace just first character
t = s.replace("R", "r")
print(t)

