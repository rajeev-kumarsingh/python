"""Sort Descending
To sort descending, use the keyword argument reverse = True:

"""
#Sort the list descending:
fruits = ["apple", "banana", "kiwi", "orange", "mango"]
fruits.sort(reverse=True)
print(fruits)

"""Output:
python3 sort-decending.py         
['orange', 'mango', 'kiwi', 'banana', 'apple']"""