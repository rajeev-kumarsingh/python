"""
Python Logical Operator
Logical operators are used to combine conditional statements:
"""
x,y = 10,20
print("'and'  x < 5 and y > 10 Return True if both statement are true: ", x < 5 and y > 50)
print("'or' x < 5 or y > 5 Return True if one of the statement is true: ", x < 10 or y > 15)
print("'not' not(x > 5 and y > 20) Reverse the result, Return False if the result is true: ", not(x > 5 and y > 20))