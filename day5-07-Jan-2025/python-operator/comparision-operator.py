"""
Python Comparison Operators
Comparison operators are used to compare two values:
"""
x = 5
y =55
print("x = 5")
print("y == 55")
print("==, x == y ", x == y)
print("!=, x != y ", x != y)
print(">, x > y ", x > y)
print("<, x < y ", x < y)
print(">=, x >= y ", x >= y)
print("<=, x <= y ", x <= y)

