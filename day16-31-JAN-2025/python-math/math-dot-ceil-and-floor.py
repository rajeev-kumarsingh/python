# the math.floor() method rounds a number downwards to its nearest integer, 
# and returns the result:
import math
x = math.ceil(5.6)
x1 = math.floor(5.6)
x2 = math.ceil(5.4)
x3 = math.floor(5.4)
print("math.ceil(5.6): ",x)
print("math.floor(5.6): ",x1)
print("math.ceil(5.4): ",x2)
print("math.floor(5.4): ",x3)
