"""
If you have collection of values in a list, tuple etc. Python allows you to extract the values into
variables. This is calles unpacking
"""
fruits = ["Apple", "Banana", "Grapes"]
fruit1, fruit2, fruit3 = fruits
print("First Name of Fruit is: ",fruit1)
print("Second Name of Fruit is: ",fruit2)
print("Third Name of Fruit is: ",fruit3)